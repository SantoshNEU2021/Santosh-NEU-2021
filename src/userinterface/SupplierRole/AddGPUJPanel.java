    private void btnAddDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDrugActionPerformed
        // TODO add your handling code here:
        String drugName = txtNameDrug.getText().trim();
        if(drugName.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"GPU name cannot be empty");
            return;
        }
        ArrayList<String> drugcheck= new ArrayList<>();
        for(Supplier d:business.getSupplierList().getSupplierList())
        {
            drugcheck.add(d.getGPUName().toLowerCase());
        }
        if(drugcheck.contains(drugName.toLowerCase()))
        {
            JOptionPane.showMessageDialog(null,"GPU name already exists");
            return;
        }

        business.getSupplierList().addSupplierList().setGPUName(drugName);
        
        populateTable();        
        log.debug(userAccount+" "+"added new GPU"+" "+drugName);
        txtNameDrug.setText("");

    }//GEN-LAST:event_btnAddDrugActionPerformed

    private void btnAddChemicalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddChemicalActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDrug.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null, "please select a row ");
            return;
        }
        Supplier c = (Supplier) tblDrug.getValueAt(selectedRow, 1);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("AddRawMaterialJPanel", new AddRawMaterialJPanel(userProcessContainer, business, userAccount, supplierOrganization, enterprise, network, c));     
        log.debug(userAccount+" "+"entering add raw materials page");   
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnAddChemicalActionPerformed


