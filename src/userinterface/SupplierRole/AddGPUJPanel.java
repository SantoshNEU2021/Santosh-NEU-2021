    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblDrug.getModel();

        model.setRowCount(0);

        for (Supplier d : business.getSupplierList().getSupplierList()) {
            Object[] row = new Object[2];
            row[0] = d.getGPUId();
            row[1] = d;
            model.addRow(row);

        }

    }

